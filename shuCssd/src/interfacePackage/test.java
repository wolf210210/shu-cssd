  public void tableload(JTable table,PublicInterface aMembers){
         String[] colName = { "Member ID", "Name"};
            Object[][] object = new Object[ obj1.size()][2];
           
            int i = 0;
            if ( aMembers.size() != 0) {
                for (PublicInterface member : aMembers) {
                    object[i][0] = member.Getlocation();
                    object[i][1] = member.GetlocationID();
                    i++; 
                }    
            }
            DefaultTableModel model = new DefaultTableModel(object,colName);   
            table.setModel(model);
            table.setRowSorter(new TableRowSorter<TableModel> (model));
            table.setAutoscrolls(true);
            table.getTableHeader().setReorderingAllowed(false);
            
    }
    
    
